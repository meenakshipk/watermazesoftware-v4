        //BitSet for measures
        BitSet bs = new BitSet(6);
        bs.set(0, jCheckBoxResTime.isSelected());
        bs.set(1, jCheckBoxRDist.isSelected());
        bs.set(2, jCheckBoxRVel.isSelected());
        bs.set(3, jCheckBoxRVelaP.isSelected());
